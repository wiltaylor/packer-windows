$providers = @(
    @{
        Name = "vmware"
        ProviderName = "vmware_desktop"
    },
    @{
        Name = "virtualbox"
        ProviderName = "virtualbox"
    }
)


task default -depends Clean, BuildAll, TestAll

$buildlist = @()
$importlist = @()
$generatorlist = @()
$cleanerlist = @()
$testlist = @()

foreach ($os in $oslist) {
    $osvmware = $os.clone()
    $osvirtualbox = $os.clone()

    $osvmware.add('HYPERVISOR', 'vmware')
    $osvirtualbox.add('HYPERVISOR', 'virtualbox')

    task "Generator$($os.OSNAME)" -depends "Clean$($os.OSNAME)"{
        .\Generators.ps1 -Generator "packerjson-base" -File ".\$($os.OSNAME)-base.json" -Properties $os
        .\Generators.ps1 -Generator "vagrantfile-base" -File ".\vagrantfile-$($os.OSNAME).template" -Properties $os
        
        .\Generators.ps1 -Generator "testscript-base" -File ".\tests\$($os.OSNAME)-vmware.tests.ps1" -Properties $osvmware
        .\Generators.ps1 -Generator "testscript-base" -File ".\tests\$($os.OSNAME)-virtualbox.tests.ps1" -Properties $osvirtualbox

        New-Item ".\floppy\$($os.OSNAME)-vmware" -ItemType Directory | out-null
        New-Item ".\floppy\$($os.OSNAME)-virtualbox" -ItemType Directory | out-null

        .\Generators.ps1 -Generator "unattend-base" -File ".\floppy\$($os.OSNAME)-vmware\autounattend.xml" -Properties $os
        .\Generators.ps1 -Generator "unattend-base" -File ".\floppy\$($os.OSNAME)-virtualbox\autounattend.xml" -Properties $os

        .\Generators.ps1 -Generator "sysprep-base" -File ".\floppy\$($os.OSNAME)-vmware\sysprep.xml" -Properties $os
        .\Generators.ps1 -Generator "sysprep-base" -File ".\floppy\$($os.OSNAME)-virtualbox\sysprep.xml" -Properties $os

        .\Generators.ps1 -Generator "build-base" -File ".\floppy\$($os.OSNAME)-vmware\build.ps1" -Properties $osvmware
        .\Generators.ps1 -Generator "build-base" -File ".\floppy\$($os.OSNAME)-virtualbox\build.ps1" -Properties $osvirtualbox

        Copy-Item ".\deploymentscript\helper.ps1" ".\floppy\$($os.OSNAME)-vmware\helper.ps1"
        Copy-Item ".\deploymentscript\helper.ps1" ".\floppy\$($os.OSNAME)-virtualbox\helper.ps1"

        Copy-Item ".\Tools\UPGRADER.EXE" ".\floppy\$($os.OSNAME)-vmware\UPGRADER.EXE"
        Copy-Item ".\Tools\UPGRADER.EXE" ".\floppy\$($os.OSNAME)-virtualbox\UPGRADER.EXE"

    }.GetNewClosure()

    task "Clean$($os.OSNAME)" {
        Remove-Item "$PSScriptRoot\$($os.OSNAME)-base.json" -Force -ErrorAction SilentlyContinue
        Remove-Item "$PSScriptRoot\vagrantfile-$($os.OSNAME).template" -Force -ErrorAction SilentlyContinue
        Remove-Item "$PSScriptRoot\$($os.OSNAME)-base-*.box -Force" -ErrorAction SilentlyContinue
        Remove-Item "$PSScriptRoot\$($os.OSNAME)-base-*" -Recurse -Force -ErrorAction SilentlyContinue
        Remove-Item "$PSScriptRoot\tests\$($os.OSNAME).tests.ps1" -ErrorAction SilentlyContinue
        Remove-Item "$PSScriptRoot\floppy\$($os.OSNAME)*" -Recurse -Force -ErrorAction SilentlyContinue
    }.GetNewClosure()

    task "Build$($os.OSNAME)" -depends "Generator$($os.OSNAME)"{
        &packer build -force ".\$($os.OSNAME)-base.json"
    }.GetNewClosure()

    task "ImportBox$($os.OSNAME)" -depends "RemoveBox$($os.OSNAME)" {
        vagrant box add --name "$($os.OSNAME)-base" ".\$($os.OSNAME)-base-vmware.box"
        vagrant box add --name "$($os.OSNAME)-base" ".\$($os.OSNAME)-base-virtualbox.box"
    }.GetNewClosure()

    task "RemoveBox$($os.OSNAME)" {
        vagrant box remove --force --provider vmware_desktop "$($os.OSNAME)-base"
        vagrant box remove --force --provider virtualbox "$($os.OSNAME)-base"
    }.GetNewClosure()

    task "Test$($os.OSNAME)" {
        Remove-Item "$PSScriptRoot\$($os.OSNAME)TestResults.xml" -ErrorAction SilentlyContinue
        Invoke-Pester -Script "$PSScriptRoot\tests\$($os.OSNAME).tests.ps1" -OutputFile "$PSScriptRoot\$($os.OSNAME)TestResults.xml" -OutputFormat NUnitXml
    }.GetNewClosure()

    $generatorlist += "Generator$($os.OSNAME)"
    $buildlist += "Build$($os.OSNAME)"
    $importlist += "ImportBox$($os.OSNAME)"
    $cleanerlist  += "Clean$($os.OSNAME)"
    $testlist += "Test$($os.OSNAME)"
}

task BuildAll -depends $buildlist
task ImportAll -depends $importlist
task GenerateAll -depends $generatorlist
task CleanAll -depends $cleanerlist
task TestAll -depends $testlist